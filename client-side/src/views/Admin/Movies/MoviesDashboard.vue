<template>
  <div class="table-wrapper p-2 movies">
    <v-row>
      <v-col cols="9">
        <h2>Movies</h2>
      </v-col>
      <v-col cols="3">
        <v-select
          solo
          v-model="selectedCinema"
          :items="getObjectOptionsName(cinemas)"
          @change="changeCinema()"
          label="Select Cinema"
        ></v-select>
      </v-col>
    </v-row>

    <hr />
    <div class="d-flex mb-5 movies-header">
      <div>
        <v-btn color="success" class="mr-2" @click="onCreateMovie()">
          <v-icon left dark> mdi-plus </v-icon>
          Create Movie
        </v-btn>
      </div>
      <div>
        <v-btn
          color="primary"
          :disabled="!isSelected"
          class="mr-2 d-lg-inline action-movie-button"
          @click="onEditClick(selected[0].id)"
        >
          Edit
        </v-btn>
        <v-btn
          color="error"
          :loading="removingMovie"
          :disabled="!isSelected || removingMovie"
          class="mr-2 d-lg-inline action-movie-button"
          @click="onDeleteClick(selected[0].id)"
        >
          Delete
          <template v-slot:loader>
            <span class="custom-loader">
              <v-icon light>mdi-cached</v-icon>
            </span>
          </template>
        </v-btn>
      </div>
      <v-btn
        :loading="loading"
        :disabled="loading"
        color="blue-grey"
        class="mt-1 mt-sm-0 ml-auto mr-0 white--text"
        @click="onRefresh()"
      >
        Refresh
        <v-icon right dark> mdi-refresh </v-icon>
        <template v-slot:loader>
          <span class="custom-loader">
            <v-icon light>mdi-cached</v-icon>
          </span>
        </template>
      </v-btn>
    </div>
    <!-- DESKTOP -->
    <div class="default-table position-relative d-lg-block">
      <v-data-table
        v-model="selected"
        :single-select="true"
        show-select
        :headers="headers"
        :items="movies"
        item-key="id"
        :items-per-page="10"
        :loading="loading"
        loading-text="Loading Movies... Please wait"
        :class="{ loaded: !loading }"
        class="elevation-1"
      >
        <template v-slot:[`item.title`]="{ item }">
          <a class="link" @click="onDetailsClick(item.id)">{{ item.title }}</a>
        </template>
        <template slot="no-data">
          <div v-if="loading" class="loading-table text-center py-1">
            <b-spinner variant="primary" />
          </div>
          <template v-else>
            <no-data
              no-data-text="No Movies have been added to this cinema yet..."
              create-text="+ Create Movie"
              access-page="Movies"
              @action="onCreateMovie()"
            />
          </template>
        </template>
      </v-data-table>
    </div>
  </div>
</template>

<script>
export default {
  name: "Movies",
  components: {},
  data() {
    return {
      selected: [],
      headers: [
        {
          text: "Id",
          align: "start",
          sortable: false,
          value: "id",
        },
        { text: "Title", value: "title" },
        { text: "Trailer Link", sortable: false, value: "trailerLink" },
        { text: "Country", value: "country" },
        { text: "Language", value: "language" },
        { text: "Genre", value: "genre" },
      ],
      selectedCinema: null,
      fields: [
        {
          key: "selected",
          label: "",
          variant: "select",
          hide: true,
        },
        { key: "id" },
        { key: "title" },
        { key: "trailerLink" },
        { key: "country" },
        { key: "language" },
        { key: "genre" },
      ],
    };
  },
  computed: {
    loading() {
      return this.$store.state.movies.loading;
    },
    movies() {
      return this.$store.state.movies.movies;
    },
    cinemas() {
      return this.$store.state.cinemas.cinemas;
    },
    removingMovie() {
      return this.$store.state.movies.removingMovie;
    },
    isSelected() {
      return this.selected[0];
    },
  },
  created() {
    this.getCinemas();
  },
  methods: {
    onRowSelected(item) {
      this.selected = item;
    },
    onRefresh() {
      this.getMovies(this.selectedCinema);
    },
    getCinemas() {
      this.$store
        .dispatch("getCinemas")
        .then(() => {
          if (this.selectedCinema == null) {
            this.selectedCinema = this.cinemas[0];
          }
          this.getMovies(this.selectedCinema);
        })
        .catch((error) => {
          this.errorToast(
            error.response?.data?.errors[0] ||
              "Something went wrong while fetching cinemas!"
          );
        });
    },
    getMovies(selectedCinema) {
      this.$store.dispatch("getMovies", selectedCinema.id).catch((error) => {
        this.errorToast(
          error.response?.data?.errors[0] ||
            "Something went wrong while fetching movies!"
        );
      });
    },
    changeCinema() {
      if (this.selectedCinema != null) {
        this.getMovies(this.selectedCinema);
      }
    },
    onEditClick(movieId) {
      this.$router.push({
        name: "movie-edit",
        params: { movieId },
      });
    },
    onDetailsClick(movieId) {
      this.$router.push({
        name: "movie-details",
        params: { movieId },
      });
    },
    onCreateMovie() {
      this.$router.push({
        name: "movie-create",
      });
    },
    onDeleteClick(movieId) {
      this.confirmDelete(
        "Delete Movie",
        "Are you sure you want to delete this movie?"
      ).then((ok) => {
        if (ok) {
          this.$store
            .dispatch("movies/removeMovie", movieId)
            .then(() => {
              this.successToast("Movie was removed");
              this.selected = [];
              this.getMovies(this.selectedCinema.id);
            })
            .catch((error) => {
              this.errorToast(error.response.data.errors[0]);
            });
        }
      });
    },
  },
};
</script>
